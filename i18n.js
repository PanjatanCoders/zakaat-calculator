// Get translated string
function t(key) {
    return (translations[currentLang] && translations[currentLang][key]) || translations.en[key] || key;
}

// Apply language to all data-i18n elements
function applyLanguage(lang) {
    currentLang = lang;

    // Set direction
    const isRTL = lang === 'ur';
    document.documentElement.lang = lang;
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';

    // Update all data-i18n text
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });

    // Update all data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });

    // Update language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    // Save preference
    localStorage.setItem('zakatLang', lang);

    // Re-render dynamic content
    if (typeof calculateZakat === 'function') calculateZakat();
    if (typeof updateDashboard === 'function') updateDashboard();
}

// Load saved language on startup
function initLanguage() {
    const saved = localStorage.getItem('zakatLang');
    if (saved && translations[saved]) {
        currentLang = saved;
    }
    applyLanguage(currentLang);
}
